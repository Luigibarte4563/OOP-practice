package mathematics;

public class arithmetic{
    
    public int add(int num1, int num2){
        int sum = num1 + num2;
        return sum;
    }

    public int sub(int num1, int num2){
        int sub = num1 - num2;
        return sub;
    }

    public int prod(int num1, int num2){
        int prod = num1 * num2;
        return prod;
    }

    public int divide(int num1, int num2){
        int divide = num1 / num2;
        return divide;
    }
}